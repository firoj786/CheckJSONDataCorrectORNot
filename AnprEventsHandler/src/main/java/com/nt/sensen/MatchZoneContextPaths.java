package com.nt.sensen;

/**
 * Represents a matching zone context path
 *
 * @author Firoj Khan
 * @since 2020-08-03
 */
public class MatchZoneContextPaths {

	/**
	 * Represents the end
	 */
	String end;

	/**
	 * Represents the start
	 */
	String start;

	public String getEnd() {
		return end;
	}

	public void setEnd(String end) {
		this.end = end;
	}

	public String getStart() {
		return start;
	}

	public void setStart(String start) {
		this.start = start;
	}

}
